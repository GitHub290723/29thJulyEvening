package dataTypes;

public class CharUse {

	public static void main(String[] args)
	{
		// Char (Always use single quote when we are declaring char value & 1 variable can only save 1 value at a time)
		//want to store gender
		//M --> Male, F --> Female
		
		char gender;
		gender='M';
		System.out.println("My gender is "+gender);
		
		// Want store my division
		
		char division;
		division='D';
		System.out.println("My division is "+division);
		
		char division1;
		division1='A';
		System.out.println("My division is "+division1+" , I was in 10th");

	}

}
