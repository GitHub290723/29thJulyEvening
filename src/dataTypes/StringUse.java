package dataTypes;

public class StringUse {

	public static void main(String[] args) 
	{
		
		// I want to store my city name
		
		String city;
		city="Pune";
		System.out.println("My city name is "+city);
		
		// I want to store my password
		
		String password;
		password="Pune@123";
		System.out.println("My password is "+password);
		

	}

}
